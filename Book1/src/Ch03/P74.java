package Ch03;

public class P74 {
	public static void main(String[] args) {
		//산술 연사자를 사용하여 총점과 평균 구하기

		int mathScore = 90;
		int engScore = 70;
		
		int totalScore = mathScore + engScore; // 총점
		System.out.println(totalScore);
		
		double avgScore = totalScore / 2.0; // 평균
		System.out.println(avgScore);
	}

}
